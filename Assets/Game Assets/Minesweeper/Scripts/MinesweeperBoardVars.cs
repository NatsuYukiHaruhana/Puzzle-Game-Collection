public static class MinesweeperBoardVars
{
    private static int width, height, nMines;

    public static int Width
    {
        get
        {
            return width;
        }
        set
        {
            width = value;
        }
    }

    public static int Height
    {
        get
        {
            return height;
        }
        set
        {
            height = value;
        }
    }

    public static int NMines
    {
        get
        {
            return nMines;
        }
        set
        {
            nMines = value;
        }
    }
}
